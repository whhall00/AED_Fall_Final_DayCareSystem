public class VitalSign {
    private double bloodPresser;
    private double heartRate;
    private double bodyFatTend;

    public double getBloodPresser() {
        return bloodPresser;
    }

    public void setBloodPresser(double bloodPresser) {
        this.bloodPresser = bloodPresser;
    }

    public double getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(double heartRate) {
        this.heartRate = heartRate;
    }

    public double getBodyFatTend() {
        return bodyFatTend;
    }

    public void setBodyFatTend(double bodyFatTend) {
        this.bodyFatTend = bodyFatTend;
    }
    
    
    
    
}
