    private void viewDetailJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailJButtonActionPerformed
        int selectRow = hospitalJTable.getSelectedRow();
        if (selectRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        HospitalEnterprise hospitalEnterprise = (HospitalEnterprise)hospitalJTable.getValueAt(selectRow,0);
        ViewHospitalJPanel viewHospitalJPanel = new ViewHospitalJPanel(userProcessContainer, hospitalEnterprise);
        userProcessContainer.add("viewHospitalJPanel", viewHospitalJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewDetailJButtonActionPerformed

    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        int row = hospitalJTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        HospitalEnterprise s = (HospitalEnterprise) hospitalJTable.getValueAt(row, 0);
        Network network = (Network)networkJComboBox.getSelectedItem();
        network.getEnterpriseDirectory().deleteEnterprise(s);
        populateTable(network);
    }//GEN-LAST:event_removeJButtonActionPerformed

