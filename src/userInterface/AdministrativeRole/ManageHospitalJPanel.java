    public void populateNetworkJComboBox() {
        networkJComboBox.removeAllItems();
        for (Network network : system.getNetworkList()) {
            networkJComboBox.addItem(network);
        }
    }

    private void populateTable(Network network) {
        DefaultTableModel dtm = (DefaultTableModel) hospitalJTable.getModel();
        dtm.setRowCount(0);
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Hospital)) {
                Object[] row = new Object[3];
                row[0] = enterprise;
                row[1] = enterprise.getEnterpriseType();
                row[2] = network.getNetworkName();
                dtm.addRow(row);
            }
        }
    }
