    private void SaveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveJButtonActionPerformed
        // TODO add your handling code here:
        //******** empty input validation*************
        if(FatherJRadioButton.isSelected())
        {
            parents.getFather().setFirstName(FatherFirstNameTxtField.getText());
            parents.getFather().setLastName(FatherLastNameTxtField.getText());
            parents.getFather().setAge(Integer.parseInt(FatherAgeTxtField.getText()));
            parents.getFather().setPhoneNum(Integer.parseInt(FatherPhoneNumTxtField.getText()));
        }
        else
        {
            parents.getMother().setFirstName(MotherFirstNameTxtField.getText());
            parents.getMother().setLastName(MotherLastNameTxtField.getText());
            parents.getMother().setAge(Integer.parseInt(MotherAgeTxtField.getText()));
            parents.getMother().setPhoneNum(Integer.parseInt(MotherPhoneNumTxtField.getText()));
        }
        
        JOptionPane.showMessageDialog(null, "Successfully saved!");
    }//GEN-LAST:event_SaveJButtonActionPerformed

